"""Module containing constants."""

import numpy as np

# --- angular quantities ---
RAD2DEG = 180 / np.pi
DEG2MIN = 60
MIN2SEC = 60
SEC2MAS = 1000
RAD2MIN = RAD2DEG * DEG2MIN
RAD2SEC = RAD2MIN * MIN2SEC
RAD2MAS = RAD2SEC * SEC2MAS
DEG2SEC = DEG2MIN * MIN2SEC
DEG2MAS = DEG2SEC * SEC2MAS
MIN2MAS = MIN2SEC * SEC2MAS

DEG2RAD = 1 / RAD2DEG
MIN2DEG = 1 / DEG2MIN
SEC2MIN = 1 / MIN2SEC
MAS2SEC = 1 / SEC2MAS
MIN2RAD = 1 / RAD2MIN
SEC2RAD = 1 / RAD2SEC
MAS2RAD = 1 / RAD2MAS
SEC2DEG = 1 / DEG2SEC
MAS2DEG = 1 / DEG2MAS
MAS2MIN = 1 / MIN2MAS

# --- wavelengths and frequencies ---
M2MICRON = 10e6
MICRON2M = 1 / M2MICRON
